"""Параметри запуску профілю AdsPower."""

from typing import Dict

# ---------------------------------------------------------------------------
# Налаштування вкладок браузера
# ---------------------------------------------------------------------------
# ``last_opened_tabs`` контролює поведінку браузера після старту профілю:
# значення ``"0"`` наказує AdsPower НЕ відновлювати вкладки з попередньої сесії.
_LAST_OPENED_TABS: str = "0"

# ---------------------------------------------------------------------------
# Налаштування захисних перевірок
# ---------------------------------------------------------------------------
# ``proxy_detection`` відповідає за автоматичне відкриття сторінки перевірки IP.
# Значення ``"0"`` блокує запуск зайвого вікна й пришвидшує старт сесії.
_PROXY_DETECTION: str = "0"

# ---------------------------------------------------------------------------
# Експортований словник із параметрами запуску
# ---------------------------------------------------------------------------
# Використовуємо окремий словник, щоб його можна було безпосередньо передавати
# у тіло запиту до AdsPower API.
START_PROFILE_PARAMETERS: Dict[str, str] = {
    "last_opened_tabs": _LAST_OPENED_TABS,
    "proxy_detection": _PROXY_DETECTION,
}
