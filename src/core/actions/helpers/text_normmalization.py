"""Утиліта нормалізації тексту для пошуку збігів."""


def text_normmalization(text: str) -> str:
    """Перетворює текст у компактну нижню форму для префіксного порівняння."""

    # Прибираємо зайві пробіли, уніфікуємо регістр і повертаємо очищений рядок.
    return " ".join((text or "").strip().split()).lower()
