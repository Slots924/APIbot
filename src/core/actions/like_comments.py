"""–ó–∞–≥–ª—É—à–∫–∞ –¥—ñ—ó, —è–∫–∞ –º–∞–ª–∞ –± —Å—Ç–∞–≤–∏—Ç–∏ –ª–∞–π–∫–∏ –Ω–∞ –∫–æ–º–µ–Ω—Ç–∞—Ä—è—Ö —É Facebook."""

from typing import Iterable, List, Optional

from selenium.webdriver.remote.webdriver import WebDriver


# –í–∏–∑–Ω–∞—á–∞—î–º–æ —Ç–∏–ø –¥–ª—è –º–∞—Å–∏–≤—É –∫–æ–º–µ–Ω—Ç–∞—Ä—ñ–≤, —â–æ–± –±—É–ª–æ –∑—Ä–æ–∑—É–º—ñ–ª–æ —è–∫—ñ –¥–∞–Ω—ñ –æ—á—ñ–∫—É—î–º–æ.
CommentList = Iterable[str]


def like_comments(driver: WebDriver, comments: Optional[CommentList] = None) -> bool:
    """–Ü–º—ñ—Ç—É—î –ø—Ä–æ—Å—Ç–∞–≤–ª–µ–Ω–Ω—è –ª–∞–π–∫—ñ–≤ –ø—ñ–¥ –∫–æ–º–µ–Ω—Ç–∞—Ä—è–º–∏ –∑—ñ —Å–ø–∏—Å–∫—É."""

    print("[ACTION like_comments] üöß –ó–∞–ø—É—â–µ–Ω–∞ –∑–∞–≥–ª—É—à–∫–∞ –¥–ª—è –ª–∞–π–∫—ñ–≤ –Ω–∞ –∫–æ–º–µ–Ω—Ç–∞—Ä—è—Ö.")

    # –Ø–∫—â–æ –≤–∏–∫–æ–Ω–∞–≤–µ—Ü—å –Ω–µ –ø–µ—Ä–µ–¥–∞–≤ –≤–ª–∞—Å–Ω–∏–π –º–∞—Å–∏–≤ —ñ–¥–µ–Ω—Ç–∏—Ñ—ñ–∫–∞—Ç–æ—Ä—ñ–≤ –∫–æ–º–µ–Ω—Ç–∞—Ä—ñ–≤,
    # –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î–º–æ —Ç–∏–ø–æ–≤–∏–π –Ω–∞–±—ñ—Ä, —â–æ–± –ø–æ–∫–∞–∑–∞—Ç–∏ –æ—á—ñ–∫—É–≤–∞–Ω–∏–π —Ñ–æ—Ä–º–∞—Ç –¥–∞–Ω–∏—Ö.
    if comments is None:
        comments_to_like: List[str] = ["com1", "com2"]
    else:
        # –Ø–≤–Ω–æ –ø–µ—Ä–µ—Ç–≤–æ—Ä—é—î–º–æ Iterable —É —Å–ø–∏—Å–æ–∫ –¥–ª—è –±–∞–≥–∞—Ç–æ—Ä–∞–∑–æ–≤–æ–≥–æ –ø—Ä–æ—Ö–æ–¥–∂–µ–Ω–Ω—è.
        comments_to_like = list(comments)

    # –ù–∞–≤—ñ—Ç—å —É –∑–∞–≥–ª—É—à—Ü—ñ –≤–∞–∂–ª–∏–≤–æ –≤—ñ–¥–æ–±—Ä–∞–∑–∏—Ç–∏ –ø–æ–≤–µ–¥—ñ–Ω–∫—É: –ø–æ—Ä–æ–∂–Ω—ñ–π –º–∞—Å–∏–≤ ‚Üí –¥—ñ—è —É—Å–ø—ñ—à–Ω–∞.
    if not comments_to_like:
        print("[ACTION like_comments] ‚ÑπÔ∏è –ú–∞—Å–∏–≤ –∫–æ–º–µ–Ω—Ç–∞—Ä—ñ–≤ –ø–æ—Ä–æ–∂–Ω—ñ–π ‚Äî –Ω–µ–º–∞—î —â–æ –ª–∞–π–∫–∞—Ç–∏.")
        return True

    # –ü—Ä–æ—Ö–æ–¥–∏–º–æ –ø–æ –∫–æ–∂–Ω–æ–º—É –∫–æ–º–µ–Ω—Ç–∞—Ä—é —Ç–∞ –ª–æ–≥—É—î–º–æ, —â–æ —Å–∞–º–µ —Ü–µ–π –µ–ª–µ–º–µ–Ω—Ç –æ—Ç—Ä–∏–º–∞–≤ –±–∏ –ª–∞–π–∫.
    for comment_id in comments_to_like:
        print(f"[ACTION like_comments] ‚ù§Ô∏è –Ü–º—ñ—Ç—É—é –ª–∞–π–∫ –¥–ª—è –∫–æ–º–µ–Ω—Ç–∞—Ä—è: {comment_id}")

    # –ó–∞–≥–ª—É—à–∫–∞ –∑–∞–≤–∂–¥–∏ –ø–æ–≤–µ—Ä—Ç–∞—î True, —â–æ–± —Å–∏–≥–Ω–∞–ª—ñ–∑—É–≤–∞—Ç–∏ —É—Å–ø—ñ—à–Ω–µ –∑–∞–≤–µ—Ä—à–µ–Ω–Ω—è –ø—Ä–æ—Ü–µ—Å—É.
    print("[ACTION like_comments] ‚úÖ –ó–∞–≥–ª—É—à–∫–∞ –∑–∞–≤–µ—Ä—à–∏–ª–∞—Å—è –±–µ–∑ –ø–æ–º–∏–ª–æ–∫.")
    return True
