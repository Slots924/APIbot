"""–ó–∞–≥–ª—É—à–∫–∞ –¥—ñ—ó, —è–∫–∞ –º–∞–ª–∞ –± —Å—Ç–∞–≤–∏—Ç–∏ —Ä–µ–∞–∫—Ü—ñ—ó –Ω–∞ –∫–æ–º–µ–Ω—Ç–∞—Ä—è—Ö —É Facebook."""

from typing import Iterable, List, Optional


# –í–∏–∑–Ω–∞—á–∞—î–º–æ —Ç–∏–ø –¥–ª—è –º–∞—Å–∏–≤—É –∫–æ–º–µ–Ω—Ç–∞—Ä—ñ–≤, —â–æ–± –±—É–ª–æ –∑—Ä–æ–∑—É–º—ñ–ª–æ —è–∫—ñ –¥–∞–Ω—ñ –æ—á—ñ–∫—É—î–º–æ.
CommentList = Iterable[str]


def like_comments(comments: Optional[CommentList] = None, reaction: str = "like") -> bool:
    """–Ü–º—ñ—Ç—É—î –ø—Ä–æ—Å—Ç–∞–≤–ª–µ–Ω–Ω—è –≤–∫–∞–∑–∞–Ω–æ—ó —Ä–µ–∞–∫—Ü—ñ—ó –ø—ñ–¥ –∫–æ–∂–Ω–∏–º –∫–æ–º–µ–Ω—Ç–∞—Ä–µ–º —ñ–∑ –º–∞—Å–∏–≤—É."""

    print(
        "[ACTION like_comments] üöß –ó–∞–ø—É—â–µ–Ω–∞ –∑–∞–≥–ª—É—à–∫–∞ –¥–ª—è —Ä–µ–∞–∫—Ü—ñ–π –Ω–∞ –∫–æ–º–µ–Ω—Ç–∞—Ä—è—Ö."
    )

    # –Ø–∫—â–æ –º–∞—Å–∏–≤ –∫–æ–º–µ–Ω—Ç–∞—Ä—ñ–≤ –Ω–µ –ø–µ—Ä–µ–¥–∞–ª–∏, –ø—ñ–¥—Å—Ç–∞–≤–ª—è—î–º–æ —à–∞–±–ª–æ–Ω–Ω—ñ –∑–Ω–∞—á–µ–Ω–Ω—è, —â–æ–±
    # –ø–æ–∫–∞–∑–∞—Ç–∏ –æ—á—ñ–∫—É–≤–∞–Ω–∏–π —Ñ–æ—Ä–º–∞—Ç –≤—Ö—ñ–¥–Ω–∏—Ö –¥–∞–Ω–∏—Ö –¥–ª—è –º–∞–π–±—É—Ç–Ω—å–æ—ó —Ä–µ–∞–ª—ñ–∑–∞—Ü—ñ—ó.
    if comments is None:
        comments_to_like: List[str] = ["com1", "com2"]
    else:
        # –ü–µ—Ä–µ—Ç–≤–æ—Ä—é—î–º–æ Iterable —É —Å–ø–∏—Å–æ–∫, —â–æ–± –º–æ–∂–Ω–∞ –±—É–ª–æ –±–∞–≥–∞—Ç–æ—Ä–∞–∑–æ–≤–æ —ñ—Ç–µ—Ä—É–≤–∞—Ç–∏—Å—å.
        comments_to_like = list(comments)

    # –í–∞–ª—ñ–¥—É—î–º–æ –Ω–∞–∑–≤—É —Ä–µ–∞–∫—Ü—ñ—ó: –ø—Ä–∏–±–∏—Ä–∞—î–º–æ –∑–∞–π–≤—ñ –ø—Ä–æ–±—ñ–ª–∏ —Ç–∞ –ø–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ, —â–æ –≤–æ–Ω–∞ –Ω–µ –ø–æ—Ä–æ–∂–Ω—è.
    normalized_reaction = reaction.strip().lower() or "like"

    # –Ø–∫—â–æ –Ω–µ–º–∞—î –∫–æ–º–µ–Ω—Ç–∞—Ä—ñ–≤ ‚Äî –ª–æ–≥—É—î–º–æ —ñ –ø–æ–≤–µ—Ä—Ç–∞—î–º–æ —É—Å–ø—ñ—Ö, –∞–¥–∂–µ –¥—ñ—ó –≤–∏–∫–æ–Ω—É–≤–∞—Ç–∏ –Ω–µ —Ç—Ä–µ–±–∞.
    if not comments_to_like:
        print(
            "[ACTION like_comments] ‚ÑπÔ∏è –ú–∞—Å–∏–≤ –∫–æ–º–µ–Ω—Ç–∞—Ä—ñ–≤ –ø–æ—Ä–æ–∂–Ω—ñ–π ‚Äî —Ä–µ–∞–∫—Ü—ñ—è –Ω–µ –ø–æ—Ç—Ä—ñ–±–Ω–∞."
        )
        return True

    # –Ü—Ç–µ—Ä—É—î–º–æ —Å–ø–∏—Å–æ–∫ —ñ –≤–∏–≤–æ–¥–∏–º–æ –ª–æ–≥ –¥–ª—è –∫–æ–∂–Ω–æ–≥–æ –∫–æ–º–µ–Ω—Ç–∞—Ä—è –∑ —Ä–µ–∞–∫—Ü—ñ—î—é, —è–∫—É —Ç—Ä–µ–±–∞ –ø–æ—Å—Ç–∞–≤–∏—Ç–∏.
    for comment_id in comments_to_like:
        print(
            f"[ACTION like_comments] ‚ù§Ô∏è –Ü–º—ñ—Ç—É—é —Ä–µ–∞–∫—Ü—ñ—é '{normalized_reaction}' –¥–ª—è –∫–æ–º–µ–Ω—Ç–∞—Ä—è: {comment_id}"
        )

    # –ó–∞–≥–ª—É—à–∫–∞ –∑–∞–≤–µ—Ä—à–∏–ª–∞—Å—å —É—Å–ø—ñ—à–Ω–æ, —Ç–æ–º—É –ø–æ–≤–µ—Ä—Ç–∞—î–º–æ True —è–∫ —ñ–Ω–¥–∏–∫–∞—Ç–æ—Ä –≤—ñ–¥—Å—É—Ç–Ω–æ—Å—Ç—ñ –ø–æ–º–∏–ª–æ–∫.
    print("[ACTION like_comments] ‚úÖ –ó–∞–≥–ª—É—à–∫–∞ –∑–∞–≤–µ—Ä—à–∏–ª–∞—Å—è –±–µ–∑ –ø–æ–º–∏–ª–æ–∫.")
    return True
